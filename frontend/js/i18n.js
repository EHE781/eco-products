let _lang = localStorage.getItem("ecoscan_lang") || "es";

function t(key) {
    return (TRANSLATIONS[_lang] || TRANSLATIONS.es)[key] || key;
}

function applyLang() {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const k = el.dataset.i18n;
        const v = t(k);
        if (el.tagName === "INPUT") el.placeholder = v;
        else el.innerHTML = v;
    });
    document.querySelectorAll(".lang-btn").forEach(b => b.classList.toggle("active", b.dataset.lang === _lang));
}

function changeLang(l) {
    _lang = l;
    localStorage.setItem("ecoscan_lang", l);
    applyLang();
    if (typeof list === "function") list();
}
